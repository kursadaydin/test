
public class Grup {
	
	private int id;
	private String grupName;

	private Seans seans;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getGrupName() {
		return grupName;
	}
	public void setGrupName(String grupName) {
		this.grupName = grupName;
	}
	public Seans getSeans() {
		return seans;
	}
	public void setSeans(Seans seans) {
		this.seans = seans;
	}
	
	
	
	
}
